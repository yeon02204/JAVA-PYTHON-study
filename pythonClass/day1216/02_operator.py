# ==================================================
# 파이썬 숫자형(Number Type) & 연산자 
# ==================================================

# ▶ 숫자형(Number Type)이란?
# 파이썬에서 숫자는 값을 계산하기 위한 자료형이다.
# 기본 숫자형은 int, float, complex 세 가지이다.

# --------------------------------------------------
# 1️⃣ 정수형 (int)
# --------------------------------------------------
# 소수점이 없는 숫자

a = 10
b = -5

print(a)
print(b)
print(type(a))   # int


# --------------------------------------------------
# 2️⃣ 실수형 (float)
# --------------------------------------------------
# 소수점이 있는 숫자

c = 3.14
d = -0.5

print(c)
print(d)
print(type(c))   # float


# --------------------------------------------------
# 3️⃣ 복소수형 (complex)
# --------------------------------------------------
# 실수부 + 허수부 형태의 숫자
# 허수는 j로 표현한다.

z = 2 + 3j

print(z)
print(type(z))   # complex

# 복소수는 실수부와 허수부를 가진다
print(z.real)    # 실수부
print(z.imag)    # 허수부

# --------------------------------------------------
# ▶ 복소수(complex)에서 "허수"란?
# --------------------------------------------------
# 허수는 "제곱하면 음수가 되는 수"를 의미한다.
# 현실 세계의 숫자로는 제곱해서 -1이 되는 수가 없다.

# 예시:
# 어떤 수 x가 있을 때
# x * x = -1  → 이런 수는 실수 영역에는 존재하지 않는다.

# 그래서 수학에서는 새로운 개념을 만들었다.
# 이 수를 허수 단위라고 하고, i 또는 j로 표현한다.
# 파이썬에서는 j를 사용한다.

# 즉,
# j * j = -1


# ▶ 허수 단위 확인
print(1j * 1j)     # -1j가 아니라 -1이 된다


# --------------------------------------------------
# ▶ 복소수는 "실수 + 허수"의 조합이다
# --------------------------------------------------
# a + bj 형태
# a : 실수부
# b : 허수부

z = 3 + 4j

print(z)           # 3 + 4j
print(type(z))     # complex


# --------------------------------------------------
# ▶ 복소수의 실수부와 허수부 분리
# --------------------------------------------------
# 복소수는 내부적으로 두 값을 가지고 있다.

print(z.real)      # 실수부 → 3.0
print(z.imag)      # 허수부 → 4.0


# --------------------------------------------------
# ▶ 허수부만 있는 복소수도 가능하다
# --------------------------------------------------
pure_imaginary = 5j

print(pure_imaginary)
print(type(pure_imaginary))


# --------------------------------------------------
# ▶ 복소수 연산 예제
# --------------------------------------------------
# 복소수끼리는 사칙연산이 가능하다.

z1 = 1 + 2j
z2 = 3 + 1j

print(z1 + z2)     # (1+3) + (2+1)j
print(z1 * z2)     # 분배 법칙 기반 계산


# --------------------------------------------------
# ▶ 복소수는 언제 쓰일까?
# --------------------------------------------------
# 일반적인 웹/백엔드 개발에서는 거의 사용하지 않는다.
# 하지만 아래 분야에서는 필수 개념이다.

# - 전기·전자공학 (교류 회로)
# - 신호 처리
# - 물리 시뮬레이션
# - 수학 계산 라이브러리

# 따라서:
# "자주 쓰이진 않지만, 숫자형의 한 종류로 알고만 있으면 된다."



# --------------------------------------------------
# ▶ 숫자형은 "값 + 타입"으로 관리된다
# --------------------------------------------------
# 같은 숫자처럼 보여도 타입이 다르면 다른 값이다.

print(3)
print(3.0)
print(type(3))     # int
print(type(3.0))   # float


# ==================================================
# 연산자(Operator)
# ==================================================
# 연산자는 값을 계산하거나 비교하기 위해 사용하는 기호이다.


# --------------------------------------------------
# 4️⃣ 산술 연산자
# --------------------------------------------------
# +  : 더하기
# -  : 빼기
# *  : 곱하기
# /  : 나누기 (항상 실수)
# // : 몫
# %  : 나머지
# ** : 거듭제곱

x = 7
y = 3

print(x + y)
print(x - y)
print(x * y)
print(x / y)    # 결과는 항상 float
print(x // y)
print(x % y)
print(x ** y)


# --------------------------------------------------
# ▶ 나누기 연산의 특징
# --------------------------------------------------
# / 는 정수끼리 계산해도 실수가 된다.

print(6 / 2)
print(type(6 / 2))   # float


# --------------------------------------------------
# ▶ 몫과 나머지
# --------------------------------------------------
# // : 소수점 버림
# %  : 나누고 남은 값

print(7 // 2)
print(7 % 2)


# --------------------------------------------------
# ▶ 실수 연산의 한계 (부동소수점 오차)
# --------------------------------------------------
# 실수는 정확한 값이 아니라 근사값으로 저장된다.

print(0.1 + 0.2)     # 0.3이 아님


# ==================================================
# 5️⃣ 형 변환(Type Casting)
# ==================================================
# 필요한 경우 직접 타입을 변환할 수 있다.

print(int(3.7))        # 실수 → 정수
print(float(5))        # 정수 → 실수
print(complex(3))      # 정수 → 복소수


# ==================================================
# 6️⃣ 비교 연산자
# ==================================================
# 비교 연산자는 결과로 True 또는 False를 반환한다.

a = 5
b = 3

print(a > b)
print(a < b)
print(a >= b)
print(a <= b)
print(a == b)
print(a != b)

print(type(a > b))     # bool


# --------------------------------------------------
# ▶ 비교 연산자 연쇄 (파이썬 특징)
# --------------------------------------------------
# 여러 비교를 한 번에 표현할 수 있다.

x = 10
print(1 < x < 20)


# ==================================================
# 7️⃣ 논리형(bool)
# ==================================================
# True와 False는 bool 타입이다.
# bool은 숫자형이기도 하다.

print(True)
print(False)
print(type(True))      # bool

# True는 1, False는 0처럼 동작한다
print(True + True)
print(False + True)


# ==================================================
# 8️⃣ 논리 연산자
# ==================================================
# and : 둘 다 True면 True
# or  : 하나라도 True면 True
# not : 반대값

p = True
q = False

print(p and q)
print(p or q)
print(not p)


# --------------------------------------------------
# ▶ 논리 연산자의 값 반환
# --------------------------------------------------
# 논리 연산자는 True/False가 아니라
# 마지막으로 평가된 값을 반환한다.

print(0 and 10)
print(5 and 10)
print(0 or 10)


# ==================================================
# 9️⃣ 대입 연산자
# ==================================================
# 계산 결과를 변수에 다시 저장한다.

n = 10
n += 5    # n = n + 5
print(n)

n -= 3
print(n)

n *= 2
print(n)

n /= 4
print(n)


# ==================================================
# 🔟 조건 연산자 (삼항 연산자)
# ==================================================
# 조건에 따라 값을 선택하는 표현식이다.
# 값1 if 조건 else 값2

score = 75
result = "합격" if score >= 60 else "불합격"
print(result)


# ==================================================
# ▶ 연산자 우선순위 요약
